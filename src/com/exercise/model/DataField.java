package com.exercise.model;

/**
 * Represents a data field to be utilized in the equations.
 * @author Carlos Agea
 *
 */
public class DataField {
	private String name = "";
	private String type = "";
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
